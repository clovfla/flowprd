export function exportMarkdown(content: string, label: string): void {
  const blob = new Blob([content], { type: "text/markdown" });
  downloadBlob(blob, `flowprd-${label}.md`);
}

export function exportPlainText(content: string, label: string): void {
  // Strip markdown formatting
  const plain = content
    .replace(/^#{1,6}\s+/gm, "") // headings
    .replace(/\*\*(.*?)\*\*/g, "$1") // bold
    .replace(/\*(.*?)\*/g, "$1") // italic
    .replace(/`([^`]+)`/g, "$1") // inline code
    .replace(/```[\s\S]*?```/g, "") // code blocks
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1") // links
    .replace(/^[-*]\s+/gm, "• ") // list items
    .replace(/^\d+\.\s+/gm, (match) => match) // ordered lists
    .replace(/^\|.*\|$/gm, "") // table rows
    .replace(/^---+$/gm, "") // hr
    .replace(/\n{3,}/g, "\n\n") // multiple newlines
    .trim();

  const blob = new Blob([plain], { type: "text/plain" });
  downloadBlob(blob, `flowprd-${label}.txt`);
}

