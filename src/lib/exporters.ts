export function exportNotion(content: string, label: string): void {
  // Notion-compatible markdown (clean, no HTML)
  const notionMd = content
    .replace(/^#{1,3}\s+/gm, (match) => match) // keep headings
    .replace(/\*\*(.+?)\*\*/g, "**$1**") // keep bold
    .replace(/\*(.+?)\*/g, "*$1*") // keep italic
    .replace(/^- (.+)$/gm, "- $1") // keep bullets
    .replace(/^\|(.+)\|$/gm, (match) => {
      // Clean table for Notion
      const cells = match.split("|").filter(Boolean).map((c) => c.trim());
      if (cells.every((c) => /^[-:]+$/.test(c))) return "";
      return "| " + cells.join(" | ") + " |";
    })
    .replace(/\n{3,}/g, "\n\n")
    .trim();

  const blob = new Blob([notionMd], { type: "text/markdown" });
  downloadBlob(blob, `flowprd-${label}-notion.md`);
}

export function exportPresentation(content: string, label: string): void {
  // Split content into slides by ## headings
  const sections = content.split(/^## /gm).filter(Boolean);
  const slides = sections.map((section, i) => {
    const lines = section.trim().split("\n");
    const title = lines[0]?.trim() || `Slide ${i + 1}`;
    const body = lines.slice(1).join("\n").trim();
    return { title, body };
  });

  const slidesHtml = slides
    .map(
      (slide, i) => `
    <div class="slide" id="slide-${i}" ${i > 0 ? 'style="display:none"' : ""}>
      <div class="slide-content">
        <h1>${slide.title}</h1>
        <div class="slide-body">${markdownToHtml(slide.body)}</div>
      </div>
      <div class="slide-number">${i + 1} / ${slides.length}</div>
    </div>`
    )
    .join("\n");

  const html = `<!DOCTYPE html>
<html lang="id">
<head>
<meta charset="UTF-8">
<title>FlowPRD — ${label}</title>
<style>
* { margin: 0; padding: 0; box-sizing: border-box; }
body { font-family: -apple-system, BlinkMacSystemFont, sans-serif; background: #0c0d11; color: #e4e4e7; }
.slide { width: 100vw; height: 100vh; display: flex; flex-direction: column; justify-content: center; align-items: center; padding: 60px; }
.slide-content { max-width: 800px; width: 100%; }
h1 { font-size: 2.5rem; font-weight: 700; margin-bottom: 2rem; color: #f97316; }
.slide-body { font-size: 1.2rem; line-height: 1.8; color: #a1a1aa; }
.slide-body h2 { font-size: 1.5rem; color: #e4e4e7; margin: 1.5rem 0 0.5rem; }
.slide-body h3 { font-size: 0.8rem; text-transform: uppercase; letter-spacing: 0.1em; color: #f97316; margin: 1rem 0 0.5rem; }
.slide-body ul { padding-left: 1.5rem; margin: 0.5rem 0; }
.slide-body li { margin: 0.3rem 0; }
.slide-body table { width: 100%; border-collapse: collapse; margin: 1rem 0; font-size: 0.9rem; }
.slide-body td, .slide-body th { padding: 0.5rem; border: 1px solid #22232e; text-align: left; }
.slide-body th { background: #181920; color: #e4e4e7; }
.slide-body strong { color: #e4e4e7; }
.slide-number { position: fixed; bottom: 20px; right: 30px; font-size: 0.8rem; color: #52525b; font-family: monospace; }
.nav { position: fixed; bottom: 20px; left: 30px; display: flex; gap: 8px; }
.nav button { padding: 8px 16px; background: #181920; border: 1px solid #22232e; color: #a1a1aa; border-radius: 6px; cursor: pointer; font-size: 0.8rem; }
.nav button:hover { border-color: #f97316; color: #f97316; }
</style>
</head>
<body>
${slidesHtml}
<div class="nav">
  <button onclick="prev()">← Prev</button>
  <button onclick="next()">Next →</button>
</div>
<script>
let current = 0;
const total = ${slides.length};
function show(n) {
  document.querySelectorAll('.slide').forEach(s => s.style.display = 'none');
  document.getElementById('slide-' + n).style.display = 'flex';
  current = n;
}
function next() { if (current < total - 1) show(current + 1); }
function prev() { if (current > 0) show(current - 1); }
document.addEventListener('keydown', e => {
  if (e.key === 'ArrowRight' || e.key === ' ') { e.preventDefault(); next(); }
  if (e.key === 'ArrowLeft') { e.preventDefault(); prev(); }
});
</script>
</body>
</html>`;

  const blob = new Blob([html], { type: "text/html" });
  downloadBlob(blob, `flowprd-${label}-presentation.html`);
}

function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}
